package studentdb;

public class ResumeData {

    String name;
    String email;
    String degree;
    String skills;

    public ResumeData(String name,
                      String email,
                      String degree,
                      String skills) {

        this.name = name;
        this.email = email;
        this.degree = degree;
        this.skills = skills;
    }
}